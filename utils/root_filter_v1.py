# ============================
#   Root Filter v1 — Universal
#   فلترة جذور لكل سور القرآن
# ============================

# أدوات – ضمائر – تراكيب – ألفاظ ليست جذورًا
STOP_ROOTS = {
    # أدوات شرط/استثناء
    "الا","الا","اما","اذا","اذن","اوا","ايا","احد","اخر","اين",

    # ضمائر
    "انا","انت","انتم","انتما","انتن","نحن","هم","هن","هو","هي","هما","لهم","لنا","لك","لكم","لكما",

    # تراكيب قرآنية شائعة
    "الم","ذلك","اول","اولئك","هذ","هـذ","هذي","هذيك","هؤلاء","التي","الذي","الذين","ذين",

    # ألفاظ عامة
    "شيء","شي","ناس","قوم","اول","اخر","عند","مثل","حيث","هنا","هناك",

    # بقايا تطبيع
    "يها","يات","يات","يات","يات","يات","يات",
    "نتم","انم","اسا","اصا","تكا","توا","تفا","ابم","مءم","يءم","قاا",

    # حروف جر
    "على","الى","في","عن","من","بما","كما","لما","عما","مما","بما","لما","عما",

    # ألفاظ لا تُعد جذورًا
    "اول","اولى","اولئك","احد","احدكم","احدهما","احدهن",
}

def is_valid_root(root):
    """فلترة جذور عامة لكل سور القرآن"""
    if not root:
        return False

    # استبعاد الجذور القصيرة جدًا
    if len(root) < 3:
        return False

    # استبعاد الأدوات والضمائر والتراكيب
    if root in STOP_ROOTS:
        return False

    return True
