# ------------------------------------
# 1) التطبيع الأساسي
# ------------------------------------
def normalize_token(w):
    if not w:
        return ""
    w = w.replace("أ", "ا")
    w = w.replace("إ", "ا")
    w = w.replace("آ", "ا")
    w = w.replace("ؤ", "ء")
    w = w.replace("ئ", "ء")
    w = w.replace("ة", "ه")
    w = w.replace("ى", "ي")
    return w

